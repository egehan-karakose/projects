        <p>
        Izmir is located on the Aegean coast and is Turkey's third largest city, with a population of around 4 million. It is the region's major commercial hub, with an airport and a large seaport, and has a more European feel than other Turkish cities, with many bars, cafes and restaurants dotting the waterfront. Travelers to Izmir often use the city as a base to visit other nearby places of interest such as Ku?ada??, the ruins at Ephesus and the Virgin Mary's house.</p>

<h2>History</h2>
<p>
Formerly called Smyrna, Izmir's history dates to 3000 B.C. Archaeological remains show that the Trojans, Hittites and Lydians all had settlements in Izmir until it was taken by Greeks around 1000 B.C. The Romans built Izmir into an important trade center and the city fell under Byzantine control when the Roman Empire divided. Izmir changed hands several times until the Ottomans conquered it in 1426. The city was almost completely burned to the ground in 1922, when the Turks drove out the Greeks during the Turkish War of Independence. <p>

<h2>Geography and Climate</h2>
<p>
Izmir is located on the northern coast of Turkey's Aegean region. Bordered by mountains to the south, the city is built around the immense Gulf of Izmir, the largest seaport in the region. Istanbul is the closest major city, about an hour-long plane trip or eight hours car ride away. Izmir's weather is mild, with damp winters and hot, humid summers. <p>

<h2>Transportation</h2>
<p>
Izmir is the Aegean region's central transportation point for bus, air and train travel. Adnan Menderes airport is about 10 miles to the south of city, with daily domestic and international flights by Turkish Airlines and budget airlines like Atlas Jet, Pegasus and Onur Air. The enormous and chaotic bus terminal in the city center has a comprehensive inner-city bus system as well as shuttles to Selcuk and Ku?ada?? every hour. The newly renovated trains make four daily trips to Ayd?n (in the center of the Aegean region), a daily run to Ankara and a daily trip to Band?rma to connect to the ferry to Istanbul. <p>

<h2>City Attractions</h2>
<p>
Although few of Izmir's older buildings remain, there are still interesting archaeological and historical sites. Ruins from the Greek, Roman and Byzantine periods are spread all over the city and in the surrounding hills. Izmir's Kemeralt? market is a huge bazaar in the city center that dates to the 17th century. The bazaar has restaurants and cinemas, and tourists can buy local and Turkish foods, textiles, jewelry and handicrafts. The clock tower at Konak Square, built in 1901, is considered the symbol of Izmir. <p>

<h2>Surrounding Attractions</h2>
<p>
The most popular place to visit outside of Izmir is the amazingly well-preserved ancient Roman city of Ephesus (Efes). Located near the town of Selcuk, Ephesus has remains of roads, a library and several temples and villas. Not far from Ephesus are the ruins of the Temple of Artemis, one of the Seven Wonders of the Ancient World, and Mary's House, a small dwelling that is believed to be the final home of the Virgin Mary. Other attractive tourist places include the hopping resort town of Ku?ada??, with crowded beaches, bars and nightclubs, and the sleepier towns of Çeşme and Foça, with stunning blue water bays and white sand beaches. <p>


        
